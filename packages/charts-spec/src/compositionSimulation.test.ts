import { describe, expect, it } from "vitest";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import {
  runCompositionSimulations,
  summarizeSimulations,
  type SimulationResult,
} from "./compositionSimulation";

function toMarkdownTable(results: SimulationResult[]): string {
  const header =
    "| ID | Category | Scenario | Outcome | RFC | Details |\n|----|----------|----------|---------|-----|---------|";
  const rows = results.map(
    (r) =>
      `| ${r.id} | ${r.category} | ${r.description} | **${r.outcome}** | ${r.rfcRecommendation} | ${r.details.replace(/\|/g, "\\|")} |`,
  );
  return [header, ...rows].join("\n");
}

describe("composition simulation (RFC-002 feasibility)", () => {
  const results = runCompositionSimulations();
  const summary = summarizeSimulations(results);

  it("runs all scenarios", () => {
    expect(results.length).toBeGreaterThanOrEqual(20);
  });

  it("canonical combos work today", () => {
    for (const id of ["S01", "S04", "S05"]) {
      const r = results.find((x) => x.id === id)!;
      expect(r.outcome, `${id}: ${r.details}`).toBe("works");
    }
  });

  it("documents C136 validation gates", () => {
    const results = runCompositionSimulations();
    const mustError = ["S11", "S12", "S13", "S14", "S15", "S18", "S19"];
    for (const id of mustError) {
      const r = results.find((x) => x.id === id)!;
      expect(r.outcome, `${id}: ${r.details}`).toBe("throws");
    }
    for (const id of ["S01", "S04", "S05", "S16", "S17"]) {
      const r = results.find((x) => x.id === id)!;
      expect(r.outcome, `${id}: ${r.details}`).toBe("works");
    }
  });

  const axiboardRepo = resolve(import.meta.dirname, "../../../../axiboard");

  it.skipIf(!existsSync(axiboardRepo))(
    "writes simulation report for Axiboard RFC-002",
    () => {
    const reportPath = resolve(
      axiboardRepo,
      "docs/charts/rfcs/RFC-002-composition-simulation.md",
    );
    const body = `# RFC-002 composition simulation results

> Generated by \`@axicharts/charts-spec\` — \`compositionSimulation.test.ts\`  
> Run: \`pnpm --filter @axicharts/charts-spec test compositionSimulation\`

## Summary (${new Date().toISOString().slice(0, 10)})

| Outcome | Count |
|---------|-------|
| works | ${summary.works} |
| silent_bad | ${summary.silent_bad} |
| ignored | ${summary.ignored} |
| throws | ${summary.throws} |

**Engine under test:** \`type: "cartesian"\` → \`validateCartesianSpec\` → \`blockMarksToChartProps\` → \`compilePanel\` → \`CartesianChart\`.

## Full results

${toMarkdownTable(results)}

## Remaining RFC deltas (warn / resolve / error scenarios)

${summary.gaps
  .map((r) => `- **${r.id}** (${r.description}): today \`${r.outcome}\` → RFC \`${r.rfcRecommendation}\``)
  .join("\n")}
`;
    mkdirSync(resolve(reportPath, ".."), { recursive: true });
    writeFileSync(reportPath, body, "utf8");
    expect(summary.gaps.length).toBeGreaterThan(0);
  },
  );
});
